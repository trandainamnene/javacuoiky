package bean;

public class yeuthichbean {
	private long masanpham;
	private long makhachkhang;
	public yeuthichbean(long masanpham, long makhachkhang) {
		super();
		this.masanpham = masanpham;
		this.makhachkhang = makhachkhang;
	}
	public long getMasanpham() {
		return masanpham;
	}
	public void setMasanpham(long masanpham) {
		this.masanpham = masanpham;
	}
	public long getMakhachkhang() {
		return makhachkhang;
	}
	public void setMakhachkhang(long makhachkhang) {
		this.makhachkhang = makhachkhang;
	}
	
	
	
}
